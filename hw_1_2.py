a = int(input('Введите число 1: '))
b = int(input('Введите число 2: '))
print('Результат сложения', a + b)
print('Результат вычитания', a - b)
print('Результат умножения', a * b)
print('Результат деления', a / b)
print('Результат возведения в степень', a ** b)
print('Результат целочисленного деления', a // b)
print('Результат остатка от деления', a % b)

